def Sj_Bj(input_48bit, j):
    # Extract the 6-bit section for the j-th S-box
    start = (j - 1) * 6
    end = start + 6
    Bj = input_48bit[start:end]

    row = bin2dec(Bj[0] + Bj[5])  # The first and last bits are the row number
    col = bin2dec(Bj[1:5])        # The middle four bits are the column number

    # Get the value from the S-box
    sbox_value = sbox[j - 1][row][col]

    # Convert the value to a 4-bit binary string
    Sj_Bj_output = dec2bin(sbox_value)

    # Pad the binary string with zeros if it's not 4 bits long
    Sj_Bj_output = Sj_Bj_output.zfill(4)

    return Sj_Bj_output


def generate_random_48bit_input():
    # Generate a random number in the range of 48-bit values
    random_number = random.getrandbits(48)
    # Format it as a 48-bit binary string
    random_48bit = format(random_number, '048b')
    return random_48bit


