# Conversion functions
def hex2bin(s):
    mp = {'0': "0000",
          '1': "0001",
          '2': "0010",
          '3': "0011",
          '4': "0100",
          '5': "0101",
          '6': "0110",
          '7': "0111",
          '8': "1000",
          '9': "1001",
          'A': "1010",
          'B': "1011",
          'C': "1100",
          'D': "1101",
          'E': "1110",
          'F': "1111"}
    bin = ""
    for i in range(len(s)):
        bin += mp[s[i].upper()]
    return bin


def bin2hex(s):
    mp = {"0000": '0',
          "0001": '1',
          "0010": '2',
          "0011": '3',
          "0100": '4',
          "0101": '5',
          "0110": '6',
          "0111": '7',
          "1000": '8',
          "1001": '9',
          "1010": 'A',
          "1011": 'B',
          "1100": 'C',
          "1101": 'D',
          "1110": 'E',
          "1111": 'F'}
    hex = ""
    for i in range(0, len(s), 4):
        ch = s[i:i+4]
        hex += mp[ch]
    return hex


def dec2bin(num):
    bin_str = bin(num).replace("0b", "")
    padding = '0' * ((4 - len(bin_str) % 4) % 4)
    return padding + bin_str


def bin2dec(binary_str):
    return int(binary_str, 2)

def permute(k, arr, n):
    permutation = ""
    for i in range(0, n):
        permutation += k[arr[i] - 1]
    return permutation


def xor(a, b):
    ans = ""
    for i in range(len(a)):
        ans += '0' if a[i] == b[i] else '1'
    return ans


